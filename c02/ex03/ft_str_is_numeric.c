int	ft_str_is_numeric(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0' && a != 0)
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			a = 1;
			i++;
		}
		else
		{
			a = 0;
		}
	}
	return (a);
}
