int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0' && a != 0)
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			a = 1;
			i++;
		}
		else
		{
			a = 0;
		}
	}
	return (a);
}
